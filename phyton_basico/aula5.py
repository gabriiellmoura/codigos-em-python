# Variaveis
""""
É o recurso que eu uso para armazenar valores na memoria do computador para que
eu possa ta utilizando esses valores posteriormente dentro do codigo e evitar 
repetições desnecessarias e cansativas.
"""
# Ex
int_num = int ('2')
print(int_num + 2)
# Nesse caso, se for preciso eu repetir a mesma coisa varias vezes, será mais simples.

# Exemplo classico do uso de variaveis.
nome = 'Gabriel'
idade = 21
maior_idade = idade >= 18

print('Nome', nome, 'idade', idade)
print(maior_idade)