# if / elif      / else
# se / se não se / se não
entrada = input('voce quer "entrar" ou "sair" ?')

if entrada == 'entrar':
    print('voce entrou no sistema')
elif entrada == 'sair':
    print('voce saiu do sistema')
else:
    print('voce não digitou entrar ou sair')
